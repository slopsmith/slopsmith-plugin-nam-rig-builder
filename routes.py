    @app.get("/api/plugins/nam_rig_builder/native_preset_one")
    def native_preset_one(file: str, kind: str = "nam", gain: float = 0.5):
        """A native_preset with ONE stage from a single file, to audition a
        gear's capture in isolation."""
        models_dir = (_config_dir / "nam_models") if _config_dir else None
        irs_dir = (_config_dir / "nam_irs") if _config_dir else None
        if kind in ("ir", "rs_ir"):
            p = _safe_child(irs_dir, file)
            if not p or not p.exists():
                return JSONResponse({"error": "ir not found"}, 404)
            stage = {"type": 2, "name": Path(file).stem, "path": str(p),
                     "bypassed": False,
                     "state": _state_b64({"irPath": str(p), "gain": float(gain)})}
        else:
            p = _safe_child(models_dir, file)
            if not p or not p.exists():
                return JSONResponse({"error": "model not found"}, 404)
            stage = {"type": 1, "name": Path(file).stem, "path": str(p),
                     "bypassed": False,
                     "state": _state_b64({"modelPath": str(p), "inputLevel": 1.0,
                                          "outputLevel": float(gain)})}
        return {"native_preset": {"version": 1, "chain": [stage]}}

    # ── Gear catalog (grouped by type, with parenting + photo) ────────
    @app.get("/api/plugins/nam_rig_builder/gear_catalog")
    def gear_catalog():
        """Every Rocksmith gear referenced by a mapped song, grouped by
        category, with what it's parented to (real make/model + assigned
        capture/file) and a tone3000 photo when resolvable from cache."""
        from tone3000_client import Tone3000Client
        conn = _get_conn()
        rs_map = _load_rs_to_real()
        img_idx = _tone_image_index()

        # Best preset_piece per gear: prefer a row with a file, else latest.
        rows = conn.execute(
            "SELECT rs_gear_type, kind, file, tone3000_id, id FROM preset_pieces "
            "ORDER BY id DESC"
        ).fetchall()
        best: dict[str, dict] = {}
        for gear, kind, file, t3kid, _id in rows:
            has_file = bool(file)
            cur = best.get(gear)
            if cur is None or (has_file and not cur["has_file"]):
                best[gear] = {"kind": kind, "file": file,
                              "tone3000_id": t3kid, "has_file": has_file}

        cats: dict[str, list] = {}
        for gear, b in best.items():
            info = rs_map.get(gear) or {}
            category = info.get("category") or "other"
            t3kid = b["tone3000_id"]
            meta = img_idx.get(t3kid) if t3kid else None
            cats.setdefault(category, []).append({
                "rs_gear": gear,
                "real_name": info.get("name") or gear,
                "make": info.get("make", ""),
                "model": info.get("model", ""),
                "category": category,
                "assigned": b["has_file"],
                "kind": b["kind"],
                "file": b["file"],
                "tone3000_id": t3kid,
                "tone3000_title": (meta or {}).get("title"),
                "image": (meta or {}).get("image"),
                "tone3000_url": Tone3000Client.tone_page_url(t3kid) if t3kid else None,
            })
        for lst in cats.values():
            lst.sort(key=lambda g: (not g["assigned"], (g["real_name"] or "").lower()))

        order = ["amp", "pedal", "cab", "rack", "other"]
        ordered = {c: cats[c] for c in order if c in cats}
        for c in cats:
            ordered.setdefault(c, cats[c])
        return {"categories": ordered,
                "counts": {c: len(v) for c, v in ordered.items()}}

    # ── Download a candidate to audition it (no assign) ───────────────
    @app.post("/api/plugins/nam_rig_builder/audition_candidate")
    def audition_candidate(data: dict = Body(...)):
        """Download the best model for a tone3000 tone_id into
        nam_models/nam_irs WITHOUT assigning, so the UI can audition a
        search candidate. Body {rs_gear, tone3000_id}. Returns {kind, file}."""
        tone3000_id = data.get("tone3000_id")
        if tone3000_id is None:
            return JSONResponse({"error": "tone3000_id required"}, 400)
        rs_gear = data.get("rs_gear") or "audition"
        category = (_load_rs_to_real().get(rs_gear) or {}).get("category", "amp")
        try:
            res = _download_candidate(
                tone3000_id=int(tone3000_id), is_ir=(category == "cab"),
                rs_gear=rs_gear, settings=_load_settings(),
            )
        except Exception as e:
            log.exception("audition_candidate failed")
            return JSONResponse({"error": f"{type(e).__name__}: {e}"}, 500)
        if not res:
            return JSONResponse({"error": "no downloadable model (or disk budget reached)"}, 502)
        kind, file = res
        return {"kind": kind, "file": file}

