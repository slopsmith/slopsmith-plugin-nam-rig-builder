        try:
            if path.stat().st_size == 0:
                return JSONResponse(
                    {"error": "file is a cloud-only placeholder; materialize it first"},
                    409,
                )
        except OSError:
            return JSONResponse({"error": "could not stat file"}, 500)

        if not _get_t3k_client().has_api_access:
            return JSONResponse({"error": "no tone3000 API key configured"}, 400)

        try:
            return _auto_download_for_song(filename, path)
        except ValueError as e:
            return JSONResponse({"error": f"unreadable file: {e}"}, 400)
        except Exception as e:
            log.exception("auto_download_song failed for %s", filename)
            return JSONResponse({"error": f"{type(e).__name__}: {e}"}, 500)

